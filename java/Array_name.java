import java.util.*;
public class Array_name{
    public static void main(String args[]){
        String name[]={"SARAVANAN","Ashif","vishnu","santhosh","keerthi","Ganesh"};
        int len=name.length;
        System.out.print(name[1]+""+len);
        for(int i=0;i<len;i++){
            System.out.println(name[i]);
        }
    }
}